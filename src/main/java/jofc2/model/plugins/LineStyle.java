package jofc2.model.plugins;

public class LineStyle {

	private String style;
	
	private Integer on;
	
	private Integer off;

	public String getStyle() {
		return style;
	}

	public LineStyle setStyle(String style) {
		this.style = style;
		return this;
	}

	public Integer getOn() {
		return on;
	}

	public LineStyle setOn(Integer on) {
		this.on = on;
		return this;
	}

	public Integer getOff() {
		return off;
	}

	public LineStyle setOff(Integer off) {
		this.off = off;
		return this;
	}
	
	
}
